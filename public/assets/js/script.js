$('.bi-trash').on('click', function (event) {
    event.preventDefault();
    const url = document.getElementById("delete").href

    swal({
        title: 'Are you sure?',
        text: 'This record and it`s details will be permanantly deleted!',
        icon: 'warning',
        buttons: ["Cancel", "Yes!"],
    }).then(function(value) {
        if (value) {
            window.location.href = url;
        }
    });
})
